package step1;

public class User {
	String id;
	String name;
	String password;
	
	
	// Alt + Shift + S 누르면 단축키 나옴
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
}
